#counts the number of points you have
def count_points(filename):
  """
  This function opens a text file and counts the number of dots (periods).

  Args:
      filename: The path to the text file.

  Returns:
      The number of dots found in the file.
  """
  point_count = 0
  with open(filename, "r") as file:
    for line in file:
      point_count += line.count("x_")
  return point_count

